#include "../header/libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*tab;

	if ((int)nmemb < 0 && (int)size < 0)
		return (NULL);
	tab = malloc(nmemb * size);
	if (!tab)
		return (NULL);
	ft_bzero(tab, nmemb * size);
	return (tab);
}
